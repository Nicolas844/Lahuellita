package Vista;

public class inventario13 {
    private int idtiene;
    private int aux;
    private String referencia;

    public inventario13() {
    }

    public inventario13(int idtiene, int aux, String referencia) {
        this.idtiene = idtiene;
        this.aux = aux;
        this.referencia = referencia;
    }

    public int getIdtiene() {
        return idtiene;
    }

    public void setIdtiene(int idtiene) {
        this.idtiene = idtiene;
    }

    public int getAux() {
        return aux;
    }

    public void setAux(int aux) {
        this.aux = aux;
    }

    public String getReferencia() {
        return referencia;
    }

    public void setReferencia(String referencia) {
        this.referencia = referencia;
    }
    
}
